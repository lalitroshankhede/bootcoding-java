package BasicDatatypeConditionArrayStringConcepts.rootbased;

public class BasicCal {

    public static void main(String[] args) {

        int num1=125;
        int num2=24;

        System.out.println(num1+"+"+num2+"="+(num1+num2));
        System.out.println(num1+"-"+num2+"="+(num1-num2));
        System.out.println(num1+"*"+num2+"="+(num1*num2));
        System.out.println(num1+"/"+num2+"="+(num1/num2));
        System.out.println(num1+"%"+num2+"="+(num1%num2));

    }
}
