package com.arrayex;

public class SingleDimensionArrayUsingforEach {


    public static void main(String[] args) {

        int arr[]={5,7,9,4,3,9,76,4,3};

        System.out.println("========== Print Array ===========");

        for (int array: arr) {
            System.out.println(array);
        }
    }
}
