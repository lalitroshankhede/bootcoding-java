package com.IfElseIf;

public class EvenOdd {

    public static void main(String[] args) {
        int number1=30;

        if (number1%2==0)
        {
            System.out.println("Given no is Even :"+number1);
        }
        else
        {
            System.out.println("Given no is Odd :"+number1);
        }
    }
}
