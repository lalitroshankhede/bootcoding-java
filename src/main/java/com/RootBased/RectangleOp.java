package com.RootBased;

public class RectangleOp {

    public static void main(String[] args) {


        //Area of Rectangle

        float width=5.5f,height=8.5f;
        float area_of_Rect=width*height;

        System.out.println("Area is "+width+"*"+height+"="+area_of_Rect);


        // Perimeter of Rectangle

        float peri_of_Rect=2*(width+height);
        System.out.println("Perimeter is 2 * (5.5 + 8.5)  ="+peri_of_Rect);
    }

}
